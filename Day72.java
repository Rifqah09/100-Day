package Day72;

public class Day72 {
    
    public static void main(String[] args) {
        
        int[][] angka = {
            {1, -2, 3},
            {-4, 5, -6},
            {7, -8, 9}
        };

        
        System.out.println("Bilangan Positif dalam Angka :");

        
        for (int i = 0; i < angka.length; i++) {
            for (int j = 0; j < angka[i].length; j++) {
                if (angka[i][j] > 0) {
                    System.out.print(angka[i][j] + " ");
                }
            }
        }
    




            }
        
    




    }

