package day21;
public class Day21 {
  public static void main(String[] args) {
    System.out.println("Target push up : ");
    int targetPushup = 50;
    int pushUp = 0;

    while (pushUp < targetPushup) {
      pushUp++;
      System.out.println("Anda telah melakukan : " + pushUp + "Push-up");
      System.out.println("Selamat! Anda telah mencapai target" + targetPushup + " Push-up");

    }

  }

}
